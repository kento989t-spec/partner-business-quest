def get_available_items(shop_id, partner_rank="D", partner_system=None):
    """パートナーランクに応じた商品一覧を返す"""
    rank_order = {"D": 0, "C": 1, "B": 2, "A": 3}
    current_rank = rank_order.get(partner_rank, 0)

    item_ids = SHOP_INVENTORY.get(shop_id, [])
    available = []
    for iid in item_ids:
        item = ITEMS.get(iid)
        if not item:
            continue
        required_rank = rank_order.get(item.get("rank", "D"), 0)
        if current_rank >= required_rank:
            # partner_shop商品はパートナー契約がある場合のみ表示
            if item.get("shop") == "partner_shop":
                if partner_system and partner_system.has_contract:
                    available.append(iid)
            else:
                available.append(iid)
    return available
